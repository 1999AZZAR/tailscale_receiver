# Function to create systemd timer unit
create_timer_unit() {
  local timer_file_path="/etc/systemd/system/${SERVICE_NAME}.timer"

  print_header "Creating Systemd Timer Unit"

  cat > "$timer_file_path" << EOL
[Unit]
Description=Tailscale File Receiver Timer
Documentation=https://github.com/1999AZZAR/tailscale_receiver
After=network-online.target tailscale.service
Wants=network-online.target

[Timer]
OnBootSec=30s
OnUnitActiveSec=30s
AccuracySec=1s
Persistent=true

[Install]
WantedBy=timers.target
EOL

  print_success "Timer unit created at '$timer_file_path'."
}

