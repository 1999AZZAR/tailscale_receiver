# Function to create Nautilus script for GNOME integration
create_nautilus_script() {
  local script_path="$NAUTILUS_SCRIPTS_DIR/$NAUTILUS_SCRIPT_NAME"

  # Create Nautilus scripts directory if it doesn't exist
  mkdir -p "$NAUTILUS_SCRIPTS_DIR" || {
    print_warning "Failed to create Nautilus scripts directory: $NAUTILUS_SCRIPTS_DIR"
    return 1
  }

  # Create the Nautilus script
  cat > "$script_path" << 'EOF'
#!/bin/bash
# Nautilus script to send files using Tailscale
# This script is called with selected files as arguments

# Check if any files were selected
if [ $# -eq 0 ]; then
    zenity --error --title="Tailscale Send" --text="No files selected." 2>/dev/null || \
    echo "Error: No files selected." >&2
    exit 1
fi

# Check if tailscale-send.sh exists and is executable
if [ ! -x "/usr/local/bin/tailscale-send.sh" ]; then
    zenity --error --title="Tailscale Send" --text="Tailscale sender not found. Please reinstall the tailscale-receiver package." 2>/dev/null || \
    echo "Error: Tailscale sender not found. Please reinstall the tailscale-receiver package." >&2
    exit 1
fi

# Call the tailscale sender with all selected files
exec /usr/local/bin/tailscale-send.sh "$@"
EOF

  # Make the script executable
  chmod +x "$script_path" || {
    print_warning "Failed to make Nautilus script executable: $script_path"
    return 1
  }

  print_success "Nautilus script created: $script_path"
}

# Function to perform preflight checks
preflight_checks() {
  echo "🔍 Performing preflight checks..."

  local issues_found=0

  # Check for root privileges
  if [ "$EUID" -ne 0 ]; then
    echo "❌ ERROR: This script must be run as root. Please use 'sudo ./install.sh'"
    echo "   Solution: sudo ./install.sh"
    exit 1
  fi

  # Check for systemd
  if ! command -v systemctl >/dev/null 2>&1; then
    echo "❌ ERROR: systemd is required but not found."
    echo "   This script requires a systemd-based Linux distribution."
    echo "   Compatible systems: Ubuntu, Debian, Fedora, Arch, CentOS, etc."
    exit 1
  fi

  # Check for Tailscale
  if ! command -v tailscale >/dev/null 2>&1; then
    echo "❌ ERROR: Tailscale is not installed."
    echo "   Please install Tailscale first:"
    echo "   - Ubuntu/Debian: curl -fsSL https://tailscale.com/install.sh | sh"
    echo "   - Other systems: Visit https://tailscale.com/download"
    exit 1
  fi

  # Check Tailscale status
  if ! tailscale status >/dev/null 2>&1; then
    echo "⚠️  WARNING: Tailscale is installed but not logged in."
    echo "   The service will install successfully, but you'll need to authenticate:"
    echo "   sudo tailscale up"
    echo "   Or set TS_AUTHKEY environment variable for automated setup."
    echo ""
    ((issues_found++))
  else
    echo "✅ Tailscale is installed and authenticated"
  fi

  # Check for notify-send (optional but recommended)
  if ! command -v notify-send >/dev/null 2>&1; then
    echo "⚠️  WARNING: notify-send not found (desktop notifications disabled)."
    echo "   Install libnotify-bin (Ubuntu/Debian) for desktop notifications."
    echo "   This is optional - the service will work without it."
    echo ""
  else
    echo "✅ Desktop notifications available"
  fi

  # Check for required tools
  local required_tools=("ping" "chown" "runuser")
  for tool in "${required_tools[@]}"; do
    if ! command -v "$tool" >/dev/null 2>&1; then
      echo "❌ ERROR: Required tool '$tool' not found."
      echo "   This tool is usually part of the base system. Please check your installation."
      exit 1
    fi
  done

  # Check for GUI tools (optional)
  local gui_tools=("kdialog" "zenity" "whiptail")
  local gui_available=false
  for tool in "${gui_tools[@]}"; do
    if command -v "$tool" >/dev/null 2>&1; then
      gui_available=true
      break
    fi
  done

  if [ "$gui_available" = false ]; then
    echo "⚠️  WARNING: No GUI dialog tools found (kdialog, zenity, whiptail)."
    echo "   The sender script will fall back to command-line interface."
    echo "   Install 'kdialog' (KDE), 'zenity' (GNOME), or 'whiptail' (console) for better UX."
    echo ""
  else
    echo "✅ GUI dialog tools available"
  fi

  # Check for desktop environments and file managers
  local nautilus_available=false
  if command -v nautilus >/dev/null 2>&1; then
    nautilus_available=true
    echo "✅ Nautilus (GNOME) detected - will install context menu integration"
  fi

  local dolphin_available=false
  if command -v dolphin >/dev/null 2>&1; then
    dolphin_available=true
    echo "✅ Dolphin (KDE) detected - will install context menu integration"
  fi

  if [ "$nautilus_available" = false ] && [ "$dolphin_available" = false ]; then
    echo "ℹ️  INFO: No supported file managers detected (Nautilus/Dolphin)."
    echo "   Context menu integration will be skipped."
    echo "   You can still use the command-line sender: /usr/local/bin/tailscale-send.sh"
    echo ""
  fi

  # Check if target directories are writable (simulate what the script will do)
  local test_user
  if [[ -n "${TARGET_USER:-}" ]]; then
    test_user="$TARGET_USER"
  elif [[ -n "${SUDO_USER:-}" ]]; then
    test_user="$SUDO_USER"
  else
    test_user="root"
  fi

  if id "$test_user" &>/dev/null; then
    local test_dir="/home/$test_user/Downloads"
    if [[ -d "$test_dir" ]]; then
      if [[ -w "$test_dir" ]]; then
        echo "✅ Target directory structure looks good: $test_dir"
      else
        echo "⚠️  WARNING: Target directory not writable: $test_dir"
        echo "   The service may fail to create the tailscale subdirectory."
        echo "   Check permissions: ls -ld $test_dir"
        ((issues_found++))
      fi
    else
      echo "⚠️  WARNING: Downloads directory doesn't exist: $test_dir"
      echo "   It will be created during installation."
    fi
  fi

  # Check for existing installation
  if [ -f "$SERVICE_FILE_PATH" ] || [ -f "$DEST_SCRIPT_PATH" ] || [ -f "$ENV_FILE_PATH" ]; then
    echo "ℹ️  Existing installation detected."
    echo "   A backup will be created during installation."
  fi

  # Check available disk space
  local available_space
  available_space=$(df /usr/local | awk 'NR==2 {print int($4/1024)}')  # KB to MB
  if [[ $available_space -lt 50 ]]; then
    echo "⚠️  WARNING: Low disk space available: ${available_space}MB"
    echo "   At least 50MB recommended for installation and logs."
    ((issues_found++))
  fi

  # Summary
  if [ $issues_found -eq 0 ]; then
    echo "✅ All preflight checks passed!"
  else
    echo "⚠️  $issues_found issue(s) found. Review warnings above."
    echo "   Installation can proceed but may have limited functionality."
  fi

  echo ""
}

# Function to validate user
validate_user() {
  local user="$1"

  # Check if user exists
  if ! id "$user" &>/dev/null; then
    print_error_and_exit "User '$user' does not exist"
  fi

  # Check if user has a home directory
  local user_home
  user_home=$(getent passwd "$user" | cut -d: -f6)
  if [[ -z "$user_home" || ! -d "$user_home" ]]; then
    print_error_and_exit "User '$user' does not have a valid home directory"
  fi

  # Check if Downloads directory exists or can be created
  local downloads_dir="$user_home/Downloads"
  if [[ ! -d "$downloads_dir" ]]; then
    if ! mkdir -p "$downloads_dir" 2>/dev/null; then
      print_error_and_exit "Cannot create Downloads directory for user '$user'"
    fi
    # Restore ownership
    chown "$user:$user" "$downloads_dir"
  fi

  # Verify we can write to the target directory
  local target_dir="$downloads_dir/tailscale"
  if [[ ! -d "$target_dir" ]]; then
    if ! mkdir -p "$target_dir" 2>/dev/null; then
      print_error_and_exit "Cannot create tailscale directory in Downloads for user '$user'"
    fi
  fi

  # Test write permissions
  if ! touch "$target_dir/.install_test" 2>/dev/null; then
    print_error_and_exit "No write permissions to '$target_dir'"
  fi
  rm -f "$target_dir/.install_test"

  # Restore ownership
  chown -R "$user:$user" "$target_dir"
}

# Function to get the target user
get_target_user() {
  local default_user=""
  
  # Try to get the user who ran sudo
  if [ -n "$SUDO_USER" ]; then
    default_user="$SUDO_USER"
  else
    # Fallback to the first non-root user with a home directory
    default_user=$(find /home -maxdepth 1 -type d -name "*" 2>/dev/null | head -1 | xargs basename 2>/dev/null || echo "")
  fi
  
  if [ -z "$default_user" ]; then
    print_error_and_exit "Could not determine default user. Please specify manually."
  fi
  
  if [ "${NONINTERACTIVE:-false}" = "true" ]; then
    echo "$default_user"
    return
  fi
  
  echo "" >&2
  echo "Which user should receive the Tailscale files?" >&2
  echo "This user will:" >&2
  echo "  - Own the received files" >&2
  echo "  - Receive desktop notifications" >&2
  echo "  - Have files saved to their Downloads/tailscale/ directory" >&2
  echo "" >&2
  
  while true; do
    read -r -p "Enter username [$default_user]: " target_user
    target_user=${target_user:-$default_user}
    
    # Validate the user exists
    if id "$target_user" &>/dev/null; then
      echo "" >&2
      echo "✅ User '$target_user' found." >&2
      break
    else
      echo "❌ User '$target_user' does not exist. Please try again." >&2
    fi
  done
  
  echo "$target_user"
}

# Function to create and configure the receiver script
create_receiver_script() {
  local target_user="$1"
  local target_dir="/home/$target_user/Downloads/tailscale"

  # Create the target directory and set ownership
  mkdir -p "$target_dir"
  chown "$target_user:$target_user" "$target_dir"

  # Copy the script without modification (configuration via environment file)
  install -m 0755 "$SOURCE_SCRIPT" "$DEST_SCRIPT_PATH" || {
    print_error_and_exit "Failed to install receiver script to '$DEST_SCRIPT_PATH'"
  }
}

